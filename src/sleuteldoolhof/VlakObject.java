package sleuteldoolhof;

public class VlakObject
{
    private int x_positie;
    private int y_positie;

    public void setXpos(int x) {
        this.x_positie = x;
    }
    
    public void setYpos(int y) {
        this.y_positie = y;
    }

    public int getXpos() {
        return this.x_positie;
    }

    public int getYpos() {
        return this.y_positie;
    }
}

